module ClassconstantUtilities
  
  def code_to_constant_string(code)
    constantslist = self.constants
    constantslist.each do |c|
      value = self.const_get(c)
      if(value.is_a?(Fixnum) and code == value)
        return c
      end
    end
  
    # if we got here?  return nil
    return nil
  end
  
end